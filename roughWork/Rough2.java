package roughWork;

public class Rough2 {
	private int id;
	private String name;
	private String comp;
	
	public Rough2(int id, String name, String comp) {
		this.id = id;
		this.name = name;
		this.comp = comp;
	}
	public int getName() {
		return this.id;
	}
	public void setName(int id) {
		this.id= id;
	}
}
